import math

x = 9
y = 13.5
n = 2
num_list = [4, 8, 19, 23, 44]

math.sqrt(x)        # Returns the square root of x
math.isqrt(y)       # Rounds square root number down to nearest int
math.pow(x, n)      # Returns x^n
math.ceil(y)        # Rounds up to nearest int (ceiling)
math.floor(y)       # Rounds down to nearest int (floor)
math.factorial(x)   # Returns x!
math.pi             # Pi
math.prod(num_list) # Returns product of all elements in an iterable

