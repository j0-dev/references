#include <stdio.h>

int main(void)
{
    printf("Hello world\n");    // Newline
    printf("Hello\t world\n");  // Tab
    printf("Hello\b world\n");  // Backspace
    printf("Hello\" world\n");  // Doublequote
    printf("Hello\\ world\n");  // Backslash
    printf("Hello\a world\n");  // Bell / Alert
    printf("Hello\? world\n");  // Question mark
    printf("Hello\f world\n");  // Formfeed
    printf("Hello\v world\n");  // Vertical tab
    return 0;
}